#include "libft.h"

int	ft_isalpha(int c)
{	
	if (c == 0)
		return (0);
	if ((c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}
