#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ch;

	i = 0;
	ch = c;
	while ((s[i] != ch) && (s[i]))
		i++;
	if (s[i] == ch)
		return ((char *)&s[i]);
	return (NULL);
}
